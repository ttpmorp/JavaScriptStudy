// Definição da função escrevaNome
function escrevaNome(nome) {
    console.log('Meu nome é: ' + nome);
}

// Chamada da função escrevaNome com o argumento 'Júlia'
escrevaNome('Júlia');

// Chamada da função escrevaNome com o argumento 'Bob'
escrevaNome('Bob');

// Definição da função verificarIdade
function verificarIdade(idade) {
    if (idade <= 18) {
        console.log('Menor de idade!');
    } else {
        console.log('Maior de idade!');
    }
}

// Chamada da função verificarIdade com o argumento 23
verificarIdade(23);
